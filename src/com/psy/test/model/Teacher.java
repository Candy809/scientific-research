package com.psy.test.model;

public class Teacher {
    private Long tea_id;
    private String tea_username;
    private String tea_password;
    private String tea_name;
    private String tea_age;
    private String tea_gender;
    private String tea_mobile;
    private String tea_email;
    private String tea_qq;
    private String tea_address;

    public Long getTea_id() {
        return tea_id;
    }

    public void setTea_id(Long tea_id) {
        this.tea_id = tea_id;
    }

    public String getTea_username() {
        return tea_username;
    }

    public void setTea_username(String tea_username) {
        this.tea_username = tea_username;
    }

    public String getTea_password() {
        return tea_password;
    }

    public void setTea_password(String tea_password) {
        this.tea_password = tea_password;
    }

    public String getTea_name() {
        return tea_name;
    }

    public void setTea_name(String tea_name) {
        this.tea_name = tea_name;
    }

    public String getTea_age() {
        return tea_age;
    }

    public void setTea_age(String tea_age) {
        this.tea_age = tea_age;
    }

    public String getTea_gender() {
        return tea_gender;
    }

    public void setTea_gender(String tea_gender) {
        this.tea_gender = tea_gender;
    }

    public String getTea_mobile() {
        return tea_mobile;
    }

    public void setTea_mobile(String tea_mobile) {
        this.tea_mobile = tea_mobile;
    }

    public String getTea_email() {
        return tea_email;
    }

    public void setTea_email(String tea_email) {
        this.tea_email = tea_email;
    }

    public String getTea_qq() {
        return tea_qq;
    }

    public void setTea_qq(String tea_qq) {
        this.tea_qq = tea_qq;
    }

    public String getTea_address() {
        return tea_address;
    }

    public void setTea_address(String tea_address) {
        this.tea_address = tea_address;
    }
}
